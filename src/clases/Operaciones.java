package clases;

public class Operaciones {

    public int suma(int a, int b){

        return a + b;

    }

    public int resta(int a, int b){

        return a - b;

    }

    public int multi(int a, int b){

        return a * b;

    }

    public int division(int a, int b){

        return a / b;

    }

    public void mensaje(String msg){

        System.out.println(msg);

    }

}
